public class CommentReplyBean {
    String name;
    String comment;

    public CommentReplyBean(String name, String comment) {
        this.name = name;
        this.comment = comment;
    }

    public String getName() {
        return name;
    }

    public String getComment() {
        return comment;
    }
}
